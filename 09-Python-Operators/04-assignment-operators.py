# Assignment operators
# Assignment operators are used in Python to assign values to variables.

# ******************************************************************************************

# operator =
x = 5
print('operator x = 5 : ',x)

# operator +=
x = 5
x += 5      # Equivalent to x = x + 5
print('operator x += 5 : ',x)

# operator -= 
x = 5
x -= 5     # Equivalent to x = x - 5
print('operator x -= 5 : ',x)

# operator *= 
x = 5
x *= 5     # Equivalent to x = x * 5
print('operator x *= 5 : ',x)

# operator /= 
x = 5
x /= 5     # Equivalent to x = x / 5
print('operator x /= 5 : ',x)

# operator //= 
x = 5
x //= 5     # Equivalent to x = x // 5
print('operator x //= 5 : ',x)

# operator %= 
x = 5
x %= 5     # Equivalent to x = x % 5
print('operator x %= 5 : ',x)

# operator **= 
x = 5
x **= 5     # Equivalent to x = x % 5
print('operator x **= 5 : ',x)



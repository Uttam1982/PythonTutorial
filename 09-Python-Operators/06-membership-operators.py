# Membership operators

# in and not in are the membership operators in Python.

# ****************************************************************************************** 
# They are used to test whether a value or variable is 
# found in a sequence (string, list, tuple, set and dictionary).

x ='python'
emp = {'name':'sam','age':23}

print('p in x ','p' in x) #True
print('P in x ','P' in x) # False
print("'Py' not in python: ",'Py' not in x)     #False

print('name in emp ','name' in emp) #True
print('sam in emp ','sam' in emp)   #False , check only key values



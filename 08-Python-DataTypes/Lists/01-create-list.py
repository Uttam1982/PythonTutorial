# How to create a list?
# In Python programming, a list is created by placing all the items (elements) 
# inside square brackets [], separated by commas.

# It can have any number of items and they may be of different types 
# (integer, float, string etc.).

#************************************************************************************
# empty list
my_list = []

# list of integers
my_list = [1,2,3,4,5]

#list with mixed data types
my_list = [12, 'Sara', 34.5]

# list containing another list
my_list =["sara",[10,20,30,40], ['hyd','chennai']]


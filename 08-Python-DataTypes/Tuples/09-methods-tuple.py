# Tuple Methods
# 1. Methods that add items or remove items are not available with tuple. 
# 2. Only the following two methods are available.
  # 1. count
  # 2. index

my_tuple = (10,20,30,20,40,50)

# The count() method returns the number of times the specified element appears in the list.
print("count the value of 20: ",my_tuple.count(20))

#The index() method return the index of the first matching element 
print("index of 20: ",my_tuple.index(20))




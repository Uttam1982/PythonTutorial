# pop method()
# 1. we can remove and return an item using the pop() method.
# 2. Since set is an unordered data type, there is no way of 
#    determining which item will be popped. It is completely 
#    arbitrary.

my_set = set('python')
print(my_set)

# pop an element
# Output: random element
print(my_set.pop())
print(my_set)

# pop an element
# Output: random element
print(my_set.pop())
print(my_set)


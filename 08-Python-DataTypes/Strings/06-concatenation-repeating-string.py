# Concatenation of Two or More Strings

# 1. The + operator does this in Python. 
# 2. Simply writing two string literals together also concatenates them.

# 3. The * operator can be used to repeat the string for a given number of times.

# Python String Operations

# + operator to concatenate two string
str1 = 'Hello'
str2 = 'World'
print("str1 + str2 = ",str1 + str2)

# repeating string using *

print("str1 * 3 = ",str1*3)

# Writing two string literals together also concatenates them like + operator.
string1 = 'Hello ' 'World'
print(string1)

# concatenate strings in different lines, we can use parentheses.
string1 = ('Hello '
          'World')
print(string1)


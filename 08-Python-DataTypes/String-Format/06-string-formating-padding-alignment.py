# Example : String formatting with padding and alignment

# string padding with left alignment
print("{:5}".format("sam"))

# string padding with right alignment
print("{:>5}".format("sam"))

# string padding with center alignment
print("{:^5}".format("sam"))

# string padding with center alignment
# and '*' padding character
print("{:*^5}".format("sam"))

#'0' padding character
print("{:0^5}".format("sam"))


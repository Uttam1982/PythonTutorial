# Comparison operators

# Comparison operators are used to compare values. 
# It returns either True or False according to the condition.

# ******************************************************************************************
# Example : Comparison operators in Python
x = 10
y = 12

print('x = ',x)
print('y = ',y)

# Output: x > y is False
print('x > y is',x>y)

# Output: x < y is True
print('x < y is',x<y)

# Output: x == y is False
print('x == y is',x==y)

# Output: x != y is True
print('x != y is',x!=y)

# Output: x >= y is False
print('x >= y is',x>=y)

# Output: x <= y is True
print('x <= y is',x<=y)
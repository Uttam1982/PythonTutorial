# Literal Collections
# There are four different literal collections 
# 1. List literals, 
# 2. Tuple literals, 
# 3. Dict literals, and 
# 4. Set literals.

#list
name = ["sam","joe","john","sara"] 

#tuple
numbers = (1,2,3)

#Dictionary
emp = {'id':101,'name':'John','age':26}  # key value pairs

#Set
city ={'Delhi','Mumbai','Hyderabad','Chennai'}

print(name)
print(numbers)
print(emp)
print(city)


